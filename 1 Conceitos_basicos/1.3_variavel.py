"""
Variável se refere a um nome que referencia um objeto na memória.
"""

#  A variável contém um valor inteiro
idade = 9

#  A variável contém uma string
nome = 'Python'

#  A variável contém um número decimal
saldo = 102.3

"""
Verificando que é idade, nome e saldo, por meio da função type(), que retorna o tipo de um objeto.
"""

print(type(idade))
print(type(nome))
print(type(saldo))

